    private void onChooseFile(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onChooseFile
        JFileChooser jfc = new JFileChooser();
        jfc.setFileFilter(new FileNameExtensionFilter("CSV", "csv"));
        jfc.showOpenDialog(this);
        File sel = jfc.getSelectedFile();
        if(sel != null){
            csvFile = sel;
            lbFilePath.setText(csvFile.getPath());
            
        }
    }//GEN-LAST:event_onChooseFile

