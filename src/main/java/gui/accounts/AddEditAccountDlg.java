    private void onSave(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_onSave
        String name = tfName.getText();
        if (name.length() > 0) {
            if(account == null){ // a new account is created, so make a new object
                account = new Account(name);
            }else{ // account is just updated
                account.setName(name);
            }
            
            ok = true;
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(this, "Bezeichnung ung√ºltig!", "Fehler", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_onSave

